/*
Un bucle for...of es una estructura de control en JavaScript que te permite iterar sobre los elementos de una colección, como un array o una cadena de texto. Aquí está la sintaxis básica:

JavaScript

for (variable of iterable) {
  // código a ejecutar para cada elemento
}

iterable: Es el objeto sobre el cual se quiere iterar. Puede ser un array, una cadena de texto, un set, un mapa, etc.

variable: Es la variable que se usará para almacenar el valor del elemento actual en cada iteración del bucle.
*/